#!/bin/bash

########################################################################################################################
# Start fpm background
########################################################################################################################
php-fpm7 -D

########################################################################################################################
# Start ssh
########################################################################################################################
/usr/sbin/sshd -D &

########################################################################################################################
# Start nginx
########################################################################################################################
NGINX=/usr/sbin/nginx
NGINX_CONF=/etc/nginx/nginx.conf
RESTART_COMMAND="/usr/sbin/nginx -s reload"

${NGINX} -c ${NGINX_CONF} -t && ${NGINX} -c ${NGINX_CONF} -g "daemon off;"
